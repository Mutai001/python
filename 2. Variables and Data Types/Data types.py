# Python provides several built-in data types:


# Data Type	Example	Description
# int	x = 10	Integer (whole number)
# float	y = 3.14	Decimal or floating-point number
# str	name = "Cyrus"	Text (string)
# bool	is_active = True	Boolean (True or False)
# list	fruits = ["apple", "banana"]	Ordered, mutable collection
# tuple	point = (4, 5)	Ordered, immutable collection
# dict	person = {"name": "Alice", "age": 25}	Key-value pair storage
# set	unique_nums = {1, 2, 3}	Unordered, unique elements
