# A variable is a named storage for data. You assign values using the = operator.

# ✅ Example:
# Assigning values to variables
x = 5
y = 10
name = "Alice"
is_active = True

# You can assign multiple variables in a single line.
# ✅ Example:
a, b, c = 1, 2, 3
print(a, b, c)

# You can also assign the same value to multiple variables.
# ✅ Example:
x = y = z = 0
print(x, y, z)

# You can change the value of a variable by assigning a new value.
# ✅ Example:
x = 5
print(x)
x = 10
print(x)

# Variables can store different data types such as integers, floats, strings, and booleans.
# ✅ Example:
age = 25 # Integer
price = 99.99 # Float
name = "Alice" # String
is_valid = True # Boolean

# You can use the type() function to check the data type of a variable.
# ✅ Example:
print(type(age)) # <class 'int'>
print(type(price)) # <class 'float'>
print(type(name)) # <class 'str'>
print(type(is_valid)) # <class 'bool'>

